            <h2>Historia</h2>
            <p>Un 8 de mayo de 1886 comenzaba la historia de Coca-Cola en Atlanta. El farmacéutico John S. Pemberton 
            quería crear un jarabe contra los problemas de digestión que además aportase energía, y acabó dando con 
            la bebida más famosa del mundo. La farmacia Jacobs fue la primera en comercializar el preparado a un precio 
            de 5 centavos el vaso, vendiendo unos nueve cada día. Era solo el inicio de una historia de más de 130 años.</p>
        
            <h2>Celebra 130 años</h2>
            <p>Han pasado 130 años desde que aquella bebida novedosa se sirviera por primera vez en una fuente de sodas en Atlanta, 
            Georgia. Hoy en día, el tomar una Coca-Cola implica no sólo tener entre las manos al refresco favorito del mundo, 
            sino toda una historia de esfuerzo y dedicación, un presente lleno de entusiasmo, trabajo y energía, y un porvenir 
            sólido impulsado por el compromiso de miles de empleados que constituyen el sistema Coca-Cola en cada país del orbe</p>

